// ── Helpers ──

function formatDMS(lat: number, lon: number): string {
    const fmtCoord = (value: number, pos: string, neg: string): string => {
        const abs = Math.abs(value);
        const deg = Math.floor(abs);
        const min = ((abs - deg) * 60).toFixed(1);
        return `${deg}°${min}'${value >= 0 ? pos : neg}`;
    };
    return `${fmtCoord(lat, 'N', 'S')} ${fmtCoord(lon, 'E', 'W')}`;
}

function formatDuration(hours: number): string {
    if (hours < 24) return `${hours.toFixed(1)}h`;
    const days = Math.floor(hours / 24);
    const remainingHours = Math.round(hours % 24);
    return `${days}d ${remainingHours}h`;
}

function formatDateTime(iso: string): string {
    try {
        const d = new Date(iso);
        return d.toLocaleString('en-AU', {
            weekday: 'short',
            day: 'numeric',
            month: 'short',
            hour: '2-digit',
            minute: '2-digit',
            hour12: false,
        });
    } catch {
        return iso;
    }
}

/**
 * Get the nearest tides to a reference time (before and after).
 * Returns up to 4 tide events: 2 before and 2 after the reference.
 */
function getNearestTides(
    tides: { time: string; type: string; height: number }[],
    refTime: Date,
): { time: string; type: string; height: number; delta: number }[] {
    const refMs = refTime.getTime();
    const sorted = [...tides]
        .map((t) => ({ ...t, delta: new Date(t.time).getTime() - refMs }))
        .sort((a, b) => Math.abs(a.delta) - Math.abs(b.delta));
    return sorted.slice(0, 4);
}

function formatTideSummaryText(
    tides: { time: string; type: string; height: number }[],
    refTime: Date,
    portName: string,
): string {
    const nearest = getNearestTides(tides, refTime);
    if (nearest.length === 0) return '';

    const lines: string[] = [];
    lines.push(`  ${portName}:`);
    nearest.forEach((t) => {
        const icon = t.type.toLowerCase().includes('high') ? '▲' : '▼';
        const label = t.type.toLowerCase().includes('high') ? 'HIGH' : 'LOW';
        const timeStr = formatDateTime(t.time);
        const deltaHrs = Math.abs(t.delta / 3600_000).toFixed(1);
        const beforeAfter = t.delta < 0 ? 'before' : 'after';
        lines.push(`    ${icon} ${label} ${t.height.toFixed(1)}m @ ${timeStr} (${deltaHrs}h ${beforeAfter})`);
    });
    return lines.join('\n');
}

function formatTideSummaryHTML(tides: { time: string; type: string; height: number }[], refTime: Date): string {
    const nearest = getNearestTides(tides, refTime);
    if (nearest.length === 0) return '<p style="color:#94a3b8;font-size:11px;">No data</p>';

    return nearest
        .map((t) => {
            const isHigh = t.type.toLowerCase().includes('high');
            const icon = isHigh ? '▲' : '▼';
            const color = isHigh ? '#38bdf8' : '#94a3b8';
            const label = isHigh ? 'HIGH' : 'LOW';
            const timeStr = formatDateTime(t.time);
            const deltaHrs = Math.abs(t.delta / 3600_000).toFixed(1);
            const beforeAfter = t.delta < 0 ? 'before' : 'after';
            return `<p style="margin:4px 0;color:${color};font-size:11px;">${icon} <strong>${label}</strong> ${t.height.toFixed(1)}m @ ${timeStr} <span style="color:#64748b;font-size:10px;">(${deltaHrs}h ${beforeAfter})</span></p>`;
        })
        .join('');
}
