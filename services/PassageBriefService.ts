/**
 * PassageBriefService — Generate shareable passage briefings.
 *
 * Creates a structured passage brief from the active route data,
 * formatted for sharing via the iOS share sheet (WhatsApp, iMessage,
 * Email, AirDrop).
 *
 * Two output modes:
 *   - Plain text: compact, messaging-app-friendly
 *   - HTML: rich, email/PDF-friendly
 */

import { createLogger } from '../utils/createLogger';

const log = createLogger('PassageBrief');

// ── Types ──

export interface PassageBriefData {
    routeName: string;
    origin: { name: string; lat: number; lon: number };
    destination: { name: string; lat: number; lon: number };
    viaWaypoints?: { name: string; lat: number; lon: number }[];
    departureTime: string;
    totalDistanceNM: number;
    estimatedDuration: number; // hours
    speed: number; // knots
    vesselName?: string;
    vesselType?: string;
    turnWaypoints?: {
        name: string;
        lat: number;
        lon: number;
        tws?: number;
        bng?: number;
    }[];
    isochroneNM?: number;
    /** Tide predictions for departure port */
    departureTides?: { time: string; type: string; height: number }[];
    /** Tide predictions for arrival port */
    arrivalTides?: { time: string; type: string; height: number }[];
}

export interface PassageBrief {
    title: string;
    generatedAt: string;
    data: PassageBriefData;
    textVersion: string;
    htmlVersion: string;
}

// ── Main API ──

/**
 * Generate a complete passage brief from route data.
 */
export function generatePassageBrief(data: PassageBriefData): PassageBrief {
    const title = `${data.origin.name} → ${data.destination.name}`;
    const generatedAt = new Date().toISOString();

    log.info(`[Brief] Generating: ${title} (${data.totalDistanceNM} NM)`);

    return {
        title,
        generatedAt,
        data,
        textVersion: formatBriefAsText(data),
        htmlVersion: formatBriefAsHTML(data),
    };
}

// ── Text Formatter (WhatsApp / iMessage) ──

function formatBriefAsText(data: PassageBriefData): string {
    const lines: string[] = [];

    // Header
    lines.push(`⛵ PASSAGE BRIEF`);
    lines.push(`━━━━━━━━━━━━━━━━━━`);
    if (data.vesselName) {
        lines.push(`🚢 ${data.vesselName}${data.vesselType ? ` (${data.vesselType})` : ''}`);
    }
    lines.push('');

    // Route
    lines.push(`📍 ROUTE`);
    lines.push(`  From: ${data.origin.name}`);
    lines.push(`    ${formatDMS(data.origin.lat, data.origin.lon)}`);
    if (data.viaWaypoints && data.viaWaypoints.length > 0) {
        lines.push(`  Via:`);
        data.viaWaypoints.forEach((wp, i) => {
            lines.push(`    ${i + 1}. ${wp.name} (${wp.lat.toFixed(4)}, ${wp.lon.toFixed(4)})`);
        });
    }
    lines.push(`  To: ${data.destination.name}`);
    lines.push(`    ${formatDMS(data.destination.lat, data.destination.lon)}`);
    lines.push('');

    // Stats
    lines.push(`📏 PASSAGE DATA`);
    lines.push(`  Distance: ${data.totalDistanceNM.toFixed(0)} NM`);
    lines.push(`  ETA Duration: ${formatDuration(data.estimatedDuration)}`);
    lines.push(`  Speed: ${data.speed} kts`);
    lines.push(`  Departure: ${formatDateTime(data.departureTime)}`);

    const arrivalTime = new Date(new Date(data.departureTime).getTime() + data.estimatedDuration * 3600_000);
    lines.push(`  Estimated Arrival: ${formatDateTime(arrivalTime.toISOString())}`);
    lines.push('');

    // Waypoints
    if (data.turnWaypoints && data.turnWaypoints.length > 0) {
        lines.push(`🧭 WAYPOINTS`);
        data.turnWaypoints.forEach((wp, i) => {
            const wind = wp.tws !== undefined ? ` | TWS ${wp.tws.toFixed(0)} kts` : '';
            const brg = wp.bng !== undefined ? ` | BRG ${Math.round(wp.bng)}°` : '';
            lines.push(
                `  ${i + 1}. ${wp.name || `WP${i + 1}`} (${wp.lat.toFixed(4)}, ${wp.lon.toFixed(4)})${wind}${brg}`,
            );
        });
        lines.push('');
    }

    // Tide info
    const depTime = new Date(data.departureTime);
    const arrTime = new Date(depTime.getTime() + data.estimatedDuration * 3600_000);
    const depTideSummary = data.departureTides?.length
        ? formatTideSummaryText(data.departureTides, depTime, data.origin.name)
        : null;
    const arrTideSummary = data.arrivalTides?.length
        ? formatTideSummaryText(data.arrivalTides, arrTime, data.destination.name)
        : null;

    if (depTideSummary || arrTideSummary) {
        lines.push(`🌊 TIDES`);
        if (depTideSummary) lines.push(depTideSummary);
        if (arrTideSummary) lines.push(arrTideSummary);
        lines.push('');
    }

    // Footer
    lines.push(`━━━━━━━━━━━━━━━━━━`);
    lines.push(`Generated by Thalassa Marine Weather`);
    lines.push(`${formatDateTime(new Date().toISOString())}`);

    return lines.join('\n');
}

